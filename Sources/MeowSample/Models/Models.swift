import MeowMongo
import Foundation

final class User: Model {
    var id = ObjectId()
    var email: String = ""
    var name: String = ""
    var gender: Gender?
    var favouriteNumbers: [Int] = []
    
    init(email: String, name: String, gender: Gender?) {
        self.email = email
        self.name = name
        self.gender = gender
    }
}

enum Gender: String, Embeddable {
    case male, female
}
